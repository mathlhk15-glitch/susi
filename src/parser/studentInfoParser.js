function extractInfo(lines) {
  let name = '—', school = '—';
  const extraNames = new Set(); // 담임·출력자 등 추가 개인명

  for (const { text: l } of lines) {
    // 학생 이름
    const nm = l.match(/성명\s*[：:]\s*([가-힣]{2,5})/);
    if (nm && name === '—') name = nm[1];
    // 고등학교명
    const hsm = l.match(/([가-힣A-Za-z0-9]{2,20}고등학교)/);
    if (hsm) school = hsm[1];
    // 담임성명: "담임성명 기경민" 또는 "담임 기경민"
    const tm = l.match(/담임\s*성명?\s*([가-힣]{2,4})/);
    if (tm) extraNames.add(tm[1]);
    // PDF 헤더 출력자: "동덕여자고등학교/날짜/IP/기경민" 마지막 슬래시 뒤 이름
    const hm = l.match(/\/([가-힣]{2,4})$/);
    if (hm) extraNames.add(hm[1]);
  }

  // 고등학교 못 찾으면 중·초 탐색
  if (school === '—') {
    for (const { text: l } of lines) {
      const sm = l.match(/([가-힣A-Za-z0-9]{2,20}(?:중|초)학교)/);
      if (sm && school === '—') school = sm[1];
    }
  }
  // 이름 최종 탐색
  if (name === '—') {
    for (const { text: l } of lines) {
      const nm = l.match(/성명\s*[：:]\s*([가-힣]{2,5})/);
      if (nm) { name = nm[1]; break; }
    }
  }
  // 학생 이름은 extraNames에서 제거 (학생 본인이 담임 이름과 겹칠 수 없지만 방어)
  extraNames.delete(name);
  return { name, school, extraNames };
}

