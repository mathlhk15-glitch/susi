// 예: "동덕여자고등학교/2026.04.25 14:36/10.4.***.76/기경민"
const PAGE_FOOTER_RE = [
  /반\s*\d+\s*번호\s*\d+\s*성명/,
  /\d{4}년\s*\d{1,2}월\s*\d{1,2}일\s*\d+\/\d+/,
  /[가-힣]{2,10}고등학교\s*\d{4}년/,
  /[가-힣]{2,10}고등학교\/\d{4}/,
  /\d{4}\.\d{2}\.\d{2}\s+\d{2}:\d{2}/,
  /\d{1,3}\.\d{1,3}\.\*+/,
  /출력일시|발급일/,
];
const isPageFooter = (t) => PAGE_FOOTER_RE.some(r => r.test(t));

function extractTaken(lines) {
  const taken = {};

  // 성적 행 패턴: 과목명 학점(1-6) 원점수/평균 형태
  // 예: "공통국어1 4 92/77.5 A A(27.3)..."
  // 예: "화법과 언어 3 88/72.1 B ..."
  const SCORE_ROW = /^([가-힣A-Za-z·\s\dⅠⅡ]{2,28}?)\s+([1-6])\s+[\d.]+\//;

  // 과목 세특 헤더: "(1학기)화법과언어:" / "공통국어1·공통국어2:" / "화법과 언어:"
  const SUBJ_HDR = /^[\(（]?(?:[12]학기[\)）]\s*)?([가-힣A-Za-z·\s\dⅠⅡ]{2,30}?)\s*[:：]\s*/;

  // 제외 패턴
  const SKIP = /^(?:학년|학기|교과|이수|합계|비고|원점수|석차|성취도|수강자수|과목평균|이수학점|수업일수|결석|봉사|일자|기간|사항|창의적|행동특성|해당\s*사항)/;

  let inCredit = false;

  for (const { text } of lines) {
    const ln = text.trim();

    if (/교과학습발달상황/.test(ln))  { inCredit = true;  continue; }
    if (/행동특성\s*및\s*종합의견|독서활동상황/.test(ln)) { inCredit = false; }

    if (SKIP.test(ln)) continue;

    // 성적 행에서 과목명 추출
    const sm = ln.match(SCORE_ROW);
    if (sm) {
      const raw = sm[1].trim();
      if (raw.length >= 2 && raw.length <= 28) {
        const canonical = normalizeSubjName(raw);
        taken[raw] = true;
        if (canonical !== raw) taken[canonical] = true;
        // 숫자 접미 제거 버전도 등록 (공통국어1 → 공통국어)
        const withoutNum = raw.replace(/[12]$/, '').trim();
        if (withoutNum !== raw && withoutNum.length >= 2) {
          taken[withoutNum] = true;
          const wc = normalizeSubjName(withoutNum);
          if (wc !== withoutNum) taken[wc] = true;
        }
      }
    }

    // 과목 세특 헤더에서도 추출 (더 넓은 범위)
    if (inCredit) {
      const hm = ln.match(SUBJ_HDR);
      if (hm) {
        // "공통국어1·공통국어2" 같은 복합 헤더 분리
        const combined = hm[1].trim();
        const parts = combined.split(/[·,·\/]\s*/);
        for (const part of parts) {
          const raw = part.trim();
          if (raw.length >= 2 && raw.length <= 28 && !SKIP.test(raw)) {
            const canonical = normalizeSubjName(raw);
            taken[raw] = true;
            if (canonical !== raw) taken[canonical] = true;
          }
        }
      }
    }
  }
  return taken;
}
