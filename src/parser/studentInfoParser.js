function normalizeSubjName(raw) {
  const s = raw.trim();
  if (NORMALIZE_TO_CANONICAL[s]) return NORMALIZE_TO_CANONICAL[s];
  // 숫자 변형 처리: '통합사회 1' → '통합사회1'
  const joined = s.replace(/\s+(\d)$/, '$1');
  if (NORMALIZE_TO_CANONICAL[joined]) return NORMALIZE_TO_CANONICAL[joined];
  return s;
}

