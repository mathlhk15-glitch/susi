function isTaken(reqName) {
  // 1. 직접 매치
  if (takenSubjects[reqName]) return true;

  // 2. 정규화된 이름으로 매치
  const canon = normalizeSubjName(reqName);
  if (takenSubjects[canon]) return true;

  // 2b. EXTRA_EQUIV 정방향: reqName이 2022명 → 2015 등가 이름 확인
  if (EXTRA_EQUIV[reqName]) {
    for (const alt of EXTRA_EQUIV[reqName]) {
      if (takenSubjects[alt] || takenSubjects[normalizeSubjName(alt)]) return true;
    }
  }
  // 2c. EXTRA_EQUIV 역방향: reqName이 2015명 → 2022 등가 이름 확인
  if (EXTRA_EQUIV_REV[reqName]) {
    for (const alt of EXTRA_EQUIV_REV[reqName]) {
      if (takenSubjects[alt] || takenSubjects[normalizeSubjName(alt)]) return true;
    }
  }

  // 3. takenSubjects의 각 항목을 정규화해서 비교
  for (const raw of Object.keys(takenSubjects)) {
    const rawCanon = normalizeSubjName(raw);
    if (rawCanon === reqName || rawCanon === canon) return true;

    // 부분 포함 (짧은 쪽이 긴 쪽에 포함될 때)
    const a = reqName.replace(/\s/g,''), b = raw.replace(/\s/g,'');
    const ac = canon.replace(/\s/g,''), bc = rawCanon.replace(/\s/g,'');
    if (a === b || a === bc || ac === b || ac === bc) return true;

    // 로마 숫자 제거 후 비교 (수학Ⅰ ↔ 수학, 물리학Ⅰ ↔ 물리학)
    const aBase = a.replace(/[ⅠⅡ]/g,'').replace(/[12]$/, ''), bBase = b.replace(/[ⅠⅡ]/g,'').replace(/[12]$/, '');
    if (aBase.length >= 2 && aBase === bBase) return true;

    // EXTRA_EQUIV로 taken의 각 항목에 대해서도 확인
    const checkAlts = (name) => {
      const alts = [...(EXTRA_EQUIV[name]||[]), ...(EXTRA_EQUIV_REV[name]||[])];
      return alts.some(alt => alt === reqName || alt === canon ||
        alt.replace(/\s/g,'') === a || alt.replace(/\s/g,'') === ac);
    };
    if (checkAlts(raw) || checkAlts(rawCanon)) return true;
  }

  // 4. NORMALIZE_TO_CANONICAL 역방향: reqName이 2022명인데 taken에 2015명 있는 경우
  for (const [name2022, name2015] of Object.entries(NORMALIZE_TO_CANONICAL)) {
    if (name2022 === reqName || name2015 === reqName) {
      if (takenSubjects[name2022] || takenSubjects[name2015]) return true;
      for (const raw of Object.keys(takenSubjects)) {
        const rc = normalizeSubjName(raw);
        if (rc === name2015 || rc === name2022) return true;
      }
    }
  }

  return false;
}

